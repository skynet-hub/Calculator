#This function carries the functinality of a basic calculator app

#This function handles addition
def add(n1, n2):
    '''Takes two numbers and return a sum'''
    return n1 + n2

#Function handles subtraction
def subtract(n1, n2):
    '''Takes two numbers and return the difference'''
    return n1 - n2

#Function handles division
def divide(n1, n2):
    '''Takes two numbers and return the results'''
    return n1 / n2

#Function handles multipluication
def multiply(n1, n2):
    '''Takes two numbers and return the product'''
    return n1 * n2